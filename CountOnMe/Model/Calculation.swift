import Foundation

class Calculation {
    
    func addition(firstNumber: Int, secondNumber: Int) -> String {
        let result = firstNumber + secondNumber
        return "\(result)"
    }
    
    func substraction(firstNumber: Int, secondNumber: Int) -> String {
        let result = firstNumber - secondNumber
        return "\(result)"
    }
    
}
